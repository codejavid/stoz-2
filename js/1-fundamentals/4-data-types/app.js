

// Primitive data types

// String
const name = "Jagan";

console.log(name);
console.log(typeof name);

// Number
const age = 100;

console.log(age);
console.log(typeof age);


// Boolean
const isApproved = true;

console.log(isApproved);
console.log(typeof isApproved);


// Null
const box = null;

console.log(null);
console.log(typeof null);


// Undefined
let selectedColor;

console.log(selectedColor);
console.log(typeof selectedColor);

// Reference Data 

// Array

const myArray = ["Jagan", 100, true, 55, null];

console.log(myArray);
console.log(typeof myArray);

// Object 

const person = {
    name:"Jagan",
    age:100,
    isApproved:true
};

console.log(person);
console.log(typeof person);